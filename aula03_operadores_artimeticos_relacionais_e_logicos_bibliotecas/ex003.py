#MEDIA SIMPLES DE DUAS NOTAS

nome=input('Digite o nome da fera: ')
n1=float(input('Digite a primeira nota: '))
n2=float(input('Agora fala aí a segunda: '))
m=(n1+n2)/2

print('A média da fera {} é: {}'.format(nome, m))


